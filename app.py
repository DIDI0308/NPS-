# --- 1. CARGA DE DATOS ---
@st.cache_data
def load_data():
    df = pd.read_excel('Base bruta dic.xlsx')
    df['Primary Driver'] = df['Primary Driver'].astype(str).replace('nan', 'N/A')
    df['Secondary Driver'] = df['Secondary Driver'].astype(str).replace('nan', 'N/A')
    df['Category'] = df['Category'].astype(str).replace('nan', 'N/A')
    df['Score'] = pd.to_numeric(df['Score'], errors='coerce').fillna(0)
    df['Survey Completed Date'] = pd.to_datetime(df['Survey Completed Date'])
    return df

df = load_data()

# Obtener mes din√°mico para el t√≠tulo
mes_actual = df['Survey Completed Date'].dt.month_name().iloc[0]

# --- T√çTULO PRINCIPAL ---
st.markdown(f"<h1 style='text-align: center; color: #FFD700;'>NPS 2025 - {mes_actual}</h1>", unsafe_allow_html=True)

# --- 2. RESUMEN GLOBAL (EST√ÅTICO) ---
st.subheader("üìä Resumen General")
col_est_1, col_est_2 = st.columns(2)

with col_est_1:
    data_anillo = df.groupby('Primary Driver')['Customer ID'].count().reset_index()
    fig_anillo = px.pie(data_anillo, values='Customer ID', names='Primary Driver', 
                        hole=0.6, title="Primary Driver Composition",
                        color_discrete_sequence=['#FFFF00', '#FFD700', '#FFEA00', '#FDDA0D'])
    fig_anillo.update_layout(template="plotly_dark", paper_bgcolor='rgba(0,0,0,0)', plot_bgcolor='rgba(0,0,0,0)')
    st.plotly_chart(fig_anillo, use_container_width=True)

with col_est_2:
    data_lineas = df.groupby('Primary Driver')['Score'].mean().sort_values(ascending=False).reset_index()
    fig_lineas = px.line(data_lineas, x='Primary Driver', y='Score', markers=True,
                         title="Average Score per Primary Driver")
    fig_lineas.update_traces(line_color='#FFD700', marker=dict(size=10, color='#FFD700'))
    fig_lineas.update_layout(template="plotly_dark", paper_bgcolor='rgba(0,0,0,0)', plot_bgcolor='rgba(0,0,0,0)', yaxis_range=[6, 10])
    st.plotly_chart(fig_lineas, use_container_width=True)

st.divider()

# --- 3. PANEL INTERACTIVO (DIN√ÅMICO) ---
st.sidebar.header("Filtros de Segmentaci√≥n")
driver_list = ['All'] + sorted(df['Primary Driver'].unique().tolist())
driver_select = st.sidebar.selectbox("Primary Driver:", driver_list)

cat_options = sorted([c for c in df['Category'].unique().tolist() if c != 'N/A'])
cat_select = st.sidebar.multiselect("Categor√≠as:", cat_options, default=cat_options)

# Filtrado
df_filt = df if driver_select == 'All' else df[df['Primary Driver'] == driver_select]
df_sec = df_filt[df_filt['Category'].isin(cat_select)]

st.subheader(f"üîç Detalle Segmentado: {driver_select}")
col_din_1, col_din_2, col_din_3 = st.columns(3)

with col_din_1:
    df_vis = df_filt[df_filt['Category'] != 'N/A']
    conteo_cat = df_vis['Category'].value_counts(normalize=True).reset_index()
    conteo_cat.columns = ['Category', 'Percentage']
    fig_nps = px.bar(conteo_cat, x='Category', y='Percentage', color='Category',
                     color_discrete_map={'Detractor': '#E74C3C', 'Passive': '#BDC3C7', 'Promoter': '#F1C40F'},
                     title="Category Composition %")
    fig_nps.update_layout(template="plotly_dark", paper_bgcolor='rgba(0,0,0,0)')
    st.plotly_chart(fig_nps, use_container_width=True)

with col_din_2:
    if not df_sec.empty:
        vol_data = df_sec['Secondary Driver'].value_counts().reset_index().sort_values(by='count')
        fig_vol = px.bar(vol_data, y='Secondary Driver', x='count', orientation='h', 
                         title="Volume by Secondary Driver", color_discrete_sequence=['#FFEA00'])
        fig_vol.update_layout(template="plotly_dark", paper_bgcolor='rgba(0,0,0,0)')
        st.plotly_chart(fig_vol, use_container_width=True)

with col_din_3:
    if not df_sec.empty:
        score_data = df_sec.groupby('Secondary Driver')['Score'].mean().reset_index().sort_values(by='Score', ascending=False)
        fig_score = px.bar(score_data, x='Secondary Driver', y='Score', title="Avg Score by Sec. Driver",
                           color_discrete_sequence=['#FDDA0D'])
        ymin = max(0, score_data['Score'].min() - 0.5)
        fig_score.update_layout(template="plotly_dark", paper_bgcolor='rgba(0,0,0,0)', yaxis_range=[ymin, 10])
        st.plotly_chart(fig_score, use_container_width=True)

